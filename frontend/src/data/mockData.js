// Helper function to get hotel by ID
export const getHotelById = (id) => mockHotels.find((h) => h.id === id);

// Helper function to get hotels by city
export const getHotelsByCity = (city) =>
  mockHotels.filter((h) => h.city.toLowerCase() === city.toLowerCase());

// Helper function to get hotels by state
export const getHotelsByState = (state) =>
  mockHotels.filter((h) => h.state.toLowerCase() === state.toLowerCase());

// Helper function to search hotels by destination (city or state)
export const searchHotels = (destination) => {
  const query = destination.toLowerCase().trim();
  return mockHotels.filter(
    (h) =>
      h.city.toLowerCase().includes(query) ||
      h.state.toLowerCase().includes(query)
  );
};
